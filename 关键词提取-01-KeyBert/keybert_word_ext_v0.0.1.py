import jieba
from keybert import KeyBERT
from sentence_transformers import SentenceTransformer


# 加载下载到本地的预训练模型 --------------------------------------
model = SentenceTransformer("./paraphrase-multilingual-MiniLM-L12-v2")
kw_model = KeyBERT(model=model)

# 输入需要进行关键词提取的文本，这里是一个字符串 ---------------------
doc = """
中国和美国作为全球经济体中的两大巨头，其未来的经济前景会受到多种复杂因素的影响，包括但不限于各自的政策调整、技术创新、国际关系、人口结构变化、资源分配与利用效率、市场开放度以及对全球经济增长动力的适应能力等。

对于中国而言：
- **机遇**：中国经济具有庞大的内需市场、持续的城市化进程、不断提升的科技创新能力和不断优化的产业结构。中国政府推行高质量发展策略，致力于解决不平衡不充分的发展问题，强化创新驱动，有望在产业升级、数字经济、绿色能源等方面取得突破性进展。
- **挑战**：中国需要应对人口老龄化带来的劳动力供给压力、环境可持续发展问题、金融风险管控以及外部环境不确定性增加（如国际贸易摩擦、技术封锁）等挑战。

对于美国而言：
- **优势**：美国拥有成熟的市场经济体系、强大的创新能力、灵活的资本市场以及丰富的高端人力资源。在全球科技创新和金融服务领域仍保持着显著的竞争优势。此外，美元的世界储备货币地位确保了其在国际金融体系中的核心作用。
- **挑战**：美国面临国内政治极化导致政策连续性和稳定性降低、基础设施老化、收入不平等加剧、公共债务高企、全球化逆流及其可能带来的供应链重构等问题。

总体来看，中美两国的经济前景取决于各自如何有效应对挑战并把握发展机遇。双方都有潜力保持长期增长，但实际表现将取决于未来数十年内的内外部政策调整及全球经济环境变迁。从长远看，全球治理格局的变化、新的科技革命以及可持续发展的要求都将深刻影响两国乃至全球经济版图的重塑。因此，无法简单断定哪个国家的经济前景绝对更好，而应该综合考量多维度因素及其动态演变过程。
"""

# 提取关键词 --------------------------------------------------
keywords = kw_model.extract_keywords(" ".join(jieba.lcut(doc)), top_n=10)
print(keywords)